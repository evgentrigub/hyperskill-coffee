public class IngredientAmount {
    int waterQuantity;
    int milkQuantity;
    int beansQuantity;

    public IngredientAmount(int waterQuantity, int milkQuantity, int beansQuantity) {
        this.waterQuantity = waterQuantity;
        this.milkQuantity = milkQuantity;
        this.beansQuantity = beansQuantity;
    }
}
